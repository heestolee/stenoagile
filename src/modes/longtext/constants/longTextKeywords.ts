// 랜덤 키워드 선택
export function getRandomLongTextKeyword(): { category: string; keyword: string } {
  const categories = Object.keys(LONGTEXT_KEYWORD_CATEGORIES);
  const category = categories[Math.floor(Math.random() * categories.length)];
  const keywords = LONGTEXT_KEYWORD_CATEGORIES[category];
  const keyword = keywords[Math.floor(Math.random() * keywords.length)];
  return { category, keyword };
}
